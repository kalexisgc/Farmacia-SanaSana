    public void validarBusqueda() {

    // Si el TextField tiene texto
    if (!txtBusqueda.getText().trim().isEmpty()) {

        cbxCategorias.setEnabled(false);

    } else {

        cbxCategorias.setEnabled(true);
    }

    // Si el ComboBox tiene una opción seleccionada
    if (cbxCategorias.getSelectedIndex() > 0) {

        txtBusqueda.setEnabled(false);

    } else {

        txtBusqueda.setEnabled(true);
    }
    }
    
    private void ActualizarMontoTotal() {

    double total = 0;

    for (int i = 0; i < modelo.getRowCount(); i++) {

        double subtotal =
            Double.parseDouble(
                modelo.getValueAt(i, 4).toString()
            );

        total += subtotal;
    }

    lbMontoTotal.setText(
        String.format("S/. %.2f", total)
    );   
    }
    
    private void CargarHistorialCompras() {

    Cliente funcionCliente =
            new Cliente();

    ArrayList<MHistorialVenta> lista =
            funcionCliente.ObtenerHistorialCompras(
                    MSesion.IDcliente);

    DefaultTableModel modeloHistorial =
            (DefaultTableModel)
            TblHistorial.getModel();

    modeloHistorial.setRowCount(0);

    for (MHistorialVenta h : lista) {

        modeloHistorial.addRow(
                new Object[]{

                    h.getIdVenta(),
                    h.getCantidadProductos(),
                    h.getMontoTotal(),
                    h.getFecha(),
                    
                });
    }
    }
    
    private void CargarDatosUsuario(String NombreU) {

    Usuario funcionU = new Usuario();

    ArrayList<MPerfil> lista =
            funcionU.ObtenerPerfil(NombreU);

    if (lista.isEmpty()) {

        JOptionPane.showMessageDialog(
                this,
                "Usuario no encontrado");

        return;
    }

    MPerfil u = lista.get(0);

    lbIDusuario.setText(
            String.valueOf(u.getIdUsuario()));

    lbRol.setText(
            u.getRol());

    lbFechaRegistro.setText(
            u.getFechaRegistro());

    lbNombreUsuario.setText(
            u.getNombreUsuario());

    lbNombres.setText(
            u.getNombres());

    lbApellidos.setText(
            u.getApellidos());

    lbCorreo.setText(
            u.getCorreo());

    lbTelefono.setText(
            String.valueOf(u.getTelefono()));
    }
    
    
    
    

