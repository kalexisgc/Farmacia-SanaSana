    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        // TODO add your handling code here:

    if (modelo.getRowCount() == 0) {
        JOptionPane.showMessageDialog(
                this,
                "No hay productos en la compra");
        return;
    }

    Cliente FuncionCliente = new Cliente();
    int IdCliente = FuncionCliente.ObtenerIdCliente();
    int idVenta = FuncionCliente.RegistrarVenta(IdCliente);

    if (idVenta == -1) {

        JOptionPane.showMessageDialog(
                this,
                "Error al registrar la venta: el Id no va en negativo");
        return;
    }

    for (int i = 0; i < modelo.getRowCount(); i++) {

        int idProducto =
                Integer.parseInt(
                modelo.getValueAt(i, 0).toString());

        int cantidad =
                Integer.parseInt(
                modelo.getValueAt(i, 2).toString());

        double precio =
                Double.parseDouble(
                modelo.getValueAt(i, 3).toString());

        double subtotal =
                Double.parseDouble(
                modelo.getValueAt(i, 4).toString());

        FuncionCliente.RegistrarDetalleVenta(
                idVenta,
                idProducto,
                cantidad,
                precio);
    }

    JOptionPane.showMessageDialog(
            this,
            "Compra realizada correctamente");

    FuncionCliente.ActualizarMontoTotalVenta(idVenta);
    FuncionCliente.ActualizarStockVenta(idVenta);
    CargarHistorialCompras();
    modelo.setRowCount(0);
    lbMontoTotal.setText("$$$");
    
    }//GEN-LAST:event_btnComprarActionPerformed

