    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {

            
            // Validaciones
            if (txtProducto.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this,"Ingrese el nombre del producto");
                return;
            }
            
            if (dtcFechaVencimiento.getDate() == null){
                JOptionPane.showMessageDialog(this, "Ingrese una fecha de vencimiento");
                return;
            }
            if (txtPrecioCompra.getText().trim().isEmpty()) {

               JOptionPane.showMessageDialog(this,"Ingrese el precio de compra");
               return;
            }
            if (txtPrecioVenta.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this,"Ingrese el precio de venta");
                return;
            }
            
            // definición de variables 
            String nombre = txtProducto.getText().trim();
            Date dato = dtcFechaVencimiento.getDate();
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            String fechaVencimiento = formato.format(dato);
            double precioCompra = Double.parseDouble(txtPrecioCompra.getText().trim());
            double precioVenta = Double.parseDouble(txtPrecioVenta.getText().trim());
            int stock = 0;
            String categoria =
            cboxCategoria.getSelectedItem().toString();

            String laboratorio =
            cboxLaboratorio.getSelectedItem().toString();
            
            boolean requiereReceta;

            
            // Validar valores positivos y fecha
            if (precioCompra <= 0) {
                JOptionPane.showMessageDialog(this,"El precio de compra debe ser mayor a 0");
                return;
            }
            if (precioVenta <= 0) {
               JOptionPane.showMessageDialog(this,"El precio de venta debe ser mayor a 0");
               return;
            }
            if (dato.before(new Date())) {
                JOptionPane.showMessageDialog(this,"La fecha de vencimiento no puede ser anterior a hoy");
                return;
            }
            
            // validar receta 
            if (rbtnRecetaSi.isSelected()) {
                requiereReceta = true;
            } else if (rbtnRecetaNo.isSelected()) {
                requiereReceta = false;
            } else {
                JOptionPane.showMessageDialog(this,"Seleccione si requiere receta");
                return;
            }

            
            Producto funcionP = new Producto();

            int idCategoria = funcionP.ObtenerIdCategoria(categoria);
            int idLaboratorio = funcionP.ObtenerIdLaboratorio(laboratorio);

            if (idCategoria == -1) {
                JOptionPane.showMessageDialog(this,"Categoría no encontrada");
                return;
            }
            if (idLaboratorio == -1) {
                JOptionPane.showMessageDialog(this,"Laboratorio no encontrado");
                return;
            }

            
            // registra el producto 
            boolean registrado =
            funcionP.RegistrarProducto(
                nombre,
                precioCompra,
                precioVenta,
                requiereReceta,
                stock,
                fechaVencimiento,
                idLaboratorio,
                idCategoria);
            
            if (registrado) {
                JOptionPane.showMessageDialog(this,"Producto registrado correctamente");

                CargarProductos();

                // Limpiar controles
                txtProducto.setText("");
                txtPrecioCompra.setText("");
                txtPrecioVenta.setText("");
                cboxCategoria.setSelectedIndex(0);
                cboxLaboratorio.setSelectedIndex(0);
                dtcFechaVencimiento.setDate(null);

            } else {

                JOptionPane.showMessageDialog(this,"No se pudo registrar el producto");
            }
        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(this,"Precio o stock inválido");

        } catch (Exception e) {

            JOptionPane.showMessageDialog(this,"Error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

