    public void CargarProductos() {

    DefaultTableModel modelo =
            (DefaultTableModel) tblVistaProductos.getModel();

    modelo.setRowCount(0);

    Producto funcionP = new Producto();

    ArrayList<MProducto> lista =
            funcionP.ObtenerProductos("");

    for (MProducto p : lista) {
        String Receta = "";
        if(p.getReceta()){
            Receta = "Si";
        } else {Receta = "No";}

        modelo.addRow(new Object[]{

            p.getIdProducto(),
            p.getNombreProducto(),
            p.getPrecioCompra(),
            p.getPrecioVenta(),
            Receta,
            p.getStock(),
            p.getFechaVencimiento(),
            p.getLaboratorio(),
            p.getCategoria(),
            p.getFechaRegistro()

        });
        }

    }
    
